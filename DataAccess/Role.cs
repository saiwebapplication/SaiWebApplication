                        cmd.Connection = con;
                        cmd.CommandType = CommandType.StoredProcedure;
                        cmd.CommandText = "usp_Role_GetById";
                        cmd.Parameters.AddWithValue("@RoleId", roleId);
                        if (con.State == ConnectionState.Closed)
                            con.Open();
